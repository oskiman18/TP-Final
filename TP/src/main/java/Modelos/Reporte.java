package Modelos;

public class Reporte {
	
	private int userA;
	private int userI;
	private int Ttransferencias;
	private float PMTransferencias;
	private int cuentasTotales;
	private int cajasAhorro;
	private int cuentascorrientes;
	private float saldoTotal;
	
	public int getUserA() {
		return userA;
	}
	public void setUserA(int userA) {
		this.userA = userA;
	}
	public int getUserI() {
		return userI;
	}
	public void setUserI(int userI) {
		this.userI = userI;
	}
	public int getTtransferencias() {
		return Ttransferencias;
	}
	public void setTtransferencias(int ttransferencias) {
		Ttransferencias = ttransferencias;
	}
	public float getPMTransferencias() {
		return PMTransferencias;
	}
	public void setPMTransferencias(float pMTransferencias) {
		PMTransferencias = pMTransferencias;
	}
	public int getCuentasTotales() {
		return cuentasTotales;
	}
	public void setCuentasTotales(int cuentasTotales) {
		this.cuentasTotales = cuentasTotales;
	}
	public int getCajasAhorro() {
		return cajasAhorro;
	}
	public void setCajasAhorro(int cajasAhorro) {
		this.cajasAhorro = cajasAhorro;
	}
	public int getCuentascorrientes() {
		return cuentascorrientes;
	}
	public void setCuentascorrientes(int cuentascorrientes) {
		this.cuentascorrientes = cuentascorrientes;
	}
	public float getSaldoTotal() {
		return saldoTotal;
	}
	public void setSaldoTotal(float saldoTotal) {
		this.saldoTotal = saldoTotal;
	}

}
